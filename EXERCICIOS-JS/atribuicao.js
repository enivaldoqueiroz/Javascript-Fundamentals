// Operadores de Atribuição 
const a = 7
let b = 3

b += a //b = b + a - Atribuição aditiva
console.log(b);

b -= 4 //b = b - a - Atribuição subtrativa
console.log(b);

b *= 2 //b = b * a - Atribuição multiplicativa
console.log(b);

b /= 2 //b = b / a - Atribuição divisiva
console.log(b);
b %= 2 //b = b - a - Atribuição modular
console.log(b);